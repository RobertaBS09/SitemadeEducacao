    private void botaoCriarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCriarActionPerformed

        Map<String, String> aluno = new HashMap<>();
        aluno.put("id", campoId.getText());
        aluno.put("nome", campoNome.getText());
        aluno.put("sobrenome", campoSobrenome.getText());
        aluno.put("email", campoEmail.getText());
        aluno.put("cidade", campoCidade.getText());
        
        alunos.add(aluno); 
        
        JOptionPane.showMessageDialog(this, "Aluno criado com sucesso!");
        limparCampos(); 

    }//GEN-LAST:event_botaoCriarActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

    String id = campoId.getText();
        for (Map<String, String> aluno : alunos) {
            if (aluno.get("id").equals(id)) {
                campoNome.setText(aluno.get("nome"));
                campoSobrenome.setText(aluno.get("sobrenome"));
                campoEmail.setText(aluno.get("email"));
                campoCidade.setText(aluno.get("cidade"));
                JOptionPane.showMessageDialog(this, "Aluno encontrado!");
                return;
            }
        }
        JOptionPane.showMessageDialog(this, "Aluno n√£o encontrado!");

    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

String id = campoId.getText();
        for (Map<String, String> aluno : alunos) {
            if (aluno.get("id").equals(id)) {
                aluno.put("nome", campoNome.getText());
                aluno.put("sobrenome", campoSobrenome.getText());
                aluno.put("email", campoEmail.getText());
                aluno.put("cidade", campoCidade.getText());
                JOptionPane.showMessageDialog(this, "Aluno atualizado com sucesso!");
                limparCampos();
                return;
            }
        }
        JOptionPane.showMessageDialog(this, "Aluno n√£o encontrado para atualizar!");

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
         String id = campoId.getText();
        Iterator<Map<String, String>> iterator = alunos.iterator();
        while (iterator.hasNext()) {
            Map<String, String> aluno = iterator.next();
            if (aluno.get("id").equals(id)) {
                iterator.remove();
                JOptionPane.showMessageDialog(this, "Aluno exclu√≠do com sucesso!");
                limparCampos();
                return;
            }
        }
        JOptionPane.showMessageDialog(this, "Aluno n√£o encontrado para excluir!");
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

